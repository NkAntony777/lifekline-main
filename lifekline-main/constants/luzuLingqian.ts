export const drawLingqian = (): LuZuLingqian => {
  const randomIndex = Math.floor(Math.random() * LUZU_LINGQIAN.length);
  return LUZU_LINGQIAN[randomIndex];
};

export const throwShengbei = (): ShengbeiResult => {
  const random = Math.random();
  if (random < 0.6) return "shengbei";
  if (random < 0.8) return "xiaobei";
  if (random < 0.95) return "yangbei";
  return "yinbei";
};

